package parser.ocl;

import org.antlr.runtime.Token;

public class ASTTuplePart extends AST {
    private Token fName;
    private ASTType fType;

    public ASTTuplePart(Token name, ASTType type) {
        fName = name;
        fType = type;
    }

    public Token name() {
        return fName;
    }

    public ASTType type() {
        return fType;
    }
}

